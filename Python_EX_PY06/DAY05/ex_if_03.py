## [실습 1] 숫자가 양수, 0 ,음수인지 판별 
num = 9
if num > 0 :
    print('양')
elif num < 0 :
    print('음')
else :
    print('0')

# 조건부 표현식
result = '양' if num > 0 else '음' if num < 0 else '0'
print(result)