# [실습] 출력하고 싶은 단을 입력 받아 해당 단의 구구단을 출력하세요
# ex) 2 * 1 = 2

gugudan = int(input("출력 단 ex) 2 : "))

for gu in range(1,10) :
    if gu % 3 :
        print(f"{gugudan:^3} * {gu:^3} = {gugudan * gu:^5}", end = "  ")
    else : 
        print(f"{gugudan:^3} * {gu:^3} = {gugudan * gu:^5}")